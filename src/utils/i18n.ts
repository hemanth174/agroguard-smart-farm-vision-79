export const useTranslation = (language: string) => {
  const t = (key: string) => {
    const keys = key.split('.');
    let result = translations[language] || translations.en;
    
    for (const k of keys) {
      result = result[k];
      if (!result) return key;
    }
    
    return result;
  };
  
  return { t };
};
